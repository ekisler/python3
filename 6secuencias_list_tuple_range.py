# Las listas son mutables
mi_lista = [1, 2, 3, 4, 5]
print(mi_lista)
print(mi_lista[0])


# Las tuplas son inmutables
mi_tupla = (1, 2, 3, 4, 5)
print(mi_tupla)
print(mi_tupla[1])

# Los rangos son inmutables
mi_rango = range(1,6)
print(mi_rango)

