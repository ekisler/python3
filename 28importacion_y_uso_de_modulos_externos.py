import math
# Cálculamos la raíz cuadrada de un numero utilizando el módulo math con la función sqrt().

x = 16
y = math.sqrt(x)
print(y)

# Cálculamos el exponente de un número utilizando la función pow() del modulo math.

base = 10
exponente = 3
resultado = math.pow(base, exponente)
print(resultado)